const messagesEl = document.getElementById('messages');
const questionEl = document.getElementById('question');
const endpointEl = document.getElementById('endpoint');
const studentIdEl = document.getElementById('student-id');
const apiKeyEl = document.getElementById('api-key');
const sendBtn = document.getElementById('send-btn');
const resetBtn = document.getElementById('reset-btn');
const modeTag = document.getElementById('mode-tag');
const autoTag = document.getElementById('auto-tag');
const toolsTag = document.getElementById('tools-tag');
const toolResultsEl = document.getElementById('tool-results');
const mcpDot = document.getElementById('mcp-dot');
const ragDot = document.getElementById('rag-dot');
const memoryCount = document.getElementById('memory-count');
const currentSid = document.getElementById('current-sid');

let streamAbortController = null;
let messageCount = 0;

function escapeHtml(text) {
  return String(text)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;');
}

function addMessage(role, text, meta = '') {
  const wrapper = document.createElement('div');
  wrapper.className = `message ${role}`;
  wrapper.innerHTML = `
    <div class="avatar">${role === 'user' ? '你' : 'AI'}</div>
    <div class="bubble">
      <div class="message-text">${escapeHtml(text).replaceAll('\n', '<br />')}</div>
      ${meta ? `<div class="message-meta">${escapeHtml(meta)}</div>` : ''}
    </div>
  `;
  messagesEl.appendChild(wrapper);
  messagesEl.scrollTop = messagesEl.scrollHeight;
  return wrapper;
}

function addStreamingMessage() {
  const wrapper = document.createElement('div');
  wrapper.className = 'message assistant';
  wrapper.innerHTML = `
    <div class="avatar">AI</div>
    <div class="bubble">
      <div class="message-text streaming"></div>
      <div class="message-meta">流式输出中...</div>
    </div>
  `;
  messagesEl.appendChild(wrapper);
  messagesEl.scrollTop = messagesEl.scrollHeight;
  return wrapper;
}

function appendToStreamingMessage(wrapper, text) {
  const textEl = wrapper.querySelector('.message-text');
  textEl.textContent += text;
  messagesEl.scrollTop = messagesEl.scrollHeight;
}

function finalizeStreamingMessage(wrapper, meta) {
  const metaEl = wrapper.querySelector('.message-meta');
  metaEl.textContent = meta || '流式输出完成';
}

function setStatus({ mode, autoGenerated, toolsUsed, toolResults }) {
  modeTag.textContent = mode || '-';
  autoTag.textContent = autoGenerated ? '是' : '否';
  toolsTag.textContent = toolsUsed?.length ? toolsUsed.join('、') : '-';

  if (toolResults && Object.keys(toolResults).length) {
    toolResultsEl.textContent = JSON.stringify(toolResults, null, 2);
  } else {
    toolResultsEl.textContent = '本次未返回工具明细或未命中工具。';
  }

  mcpDot.className = `dot ${mode === 'mcp' ? 'ok' : 'muted'}`;
  ragDot.className = `dot ${mode === 'rag' ? 'ok' : 'muted'}`;
}

function apiHeaders() {
  return {
    'Content-Type': 'application/json',
    'X-API-Key': apiKeyEl.value.trim() || 'campus-qa-dev-key',
  };
}

async function askStandard(question, endpoint, student_id) {
  const res = await fetch(endpoint, {
    method: 'POST',
    headers: apiHeaders(),
    body: JSON.stringify({ question, student_id }),
  });

  const data = await res.json();
  if (!res.ok) {
    throw new Error(data?.detail || `HTTP ${res.status}`);
  }
  return data;
}

async function askStream(question, student_id, wrapper) {
  streamAbortController = new AbortController();

  const res = await fetch('/ask/stream', {
    method: 'POST',
    headers: apiHeaders(),
    body: JSON.stringify({ question, student_id }),
    signal: streamAbortController.signal,
  });

  if (!res.ok) {
    const data = await res.json().catch(() => ({}));
    throw new Error(data?.detail || `HTTP ${res.status}`);
  }

  const reader = res.body.getReader();
  const decoder = new TextDecoder();
  let buffer = '';
  let meta = null;

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;

    buffer += decoder.decode(value, { stream: true });
    const lines = buffer.split('\n');
    buffer = lines.pop() || '';

    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const payload = JSON.parse(line.slice(6));

      if (payload.event === 'meta') {
        meta = payload;
        setStatus(meta);
        finalizeStreamingMessage(wrapper, `模式：${payload.mode}${payload.auto_generated ? ' · 自动生成' : ''}`);
      } else if (payload.event === 'token') {
        appendToStreamingMessage(wrapper, payload.token);
      } else if (payload.event === 'done') {
        finalizeStreamingMessage(wrapper, meta ? `模式：${meta.mode} · 流式完成` : '');
      }
    }
  }

  streamAbortController = null;
}

async function ask() {
  const question = questionEl.value.trim();
  const endpoint = endpointEl.value;
  const student_id = studentIdEl.value.trim() || 'S001';

  if (!question) {
    questionEl.focus();
    return;
  }

  if (streamAbortController) {
    streamAbortController.abort();
    streamAbortController = null;
  }

  sendBtn.disabled = true;
  sendBtn.textContent = '发送中...';
  addMessage('user', question, `接口：${endpoint} · 学生ID：${student_id}`);
  messageCount++;
  updateMemoryCount();

  const isStreaming = endpoint === '/ask/stream';

  try {
    if (isStreaming) {
      const wrapper = addStreamingMessage();
      await askStream(question, student_id, wrapper);
    } else {
      const data = await askStandard(question, endpoint, student_id);
      const meta = `模式：${data.mode || '-'}${data.auto_generated ? ' · 自动生成' : ''}`;
      addMessage('assistant', data.answer, meta);
      setStatus(data);
    }
  } catch (error) {
    if (error.name === 'AbortError') return;
    addMessage('assistant', `请求失败：${error.message}`, '错误');
    setStatus({ mode: 'error', autoGenerated: false, toolsUsed: [], toolResults: {} });
  } finally {
    sendBtn.disabled = false;
    sendBtn.textContent = '发送';
    questionEl.focus();
  }
}

async function resetConversation() {
  const student_id = studentIdEl.value.trim() || 'S001';

  if (streamAbortController) {
    streamAbortController.abort();
    streamAbortController = null;
  }

  try {
    const res = await fetch('/reset', {
      method: 'POST',
      headers: apiHeaders(),
      body: JSON.stringify({ student_id }),
    });
    const data = await res.json();
    if (data.status === 'ok') {
      messageCount = 0;
      updateMemoryCount();
      modeTag.textContent = '已重置';
      toolsTag.textContent = '-';
      toolResultsEl.textContent = '对话历史已清除。';
      addMessage('assistant', `已清除 ${student_id} 的对话历史，可以重新开始提问了。`, '系统');
    }
  } catch (error) {
    addMessage('assistant', `重置失败：${error.message}`, '错误');
  }
}

function updateMemoryCount() {
  memoryCount.textContent = messageCount;
}

// Event listeners
document.querySelectorAll('[data-preset]').forEach((btn) => {
  btn.addEventListener('click', () => {
    questionEl.value = btn.getAttribute('data-preset');
    questionEl.focus();
  });
});

document.querySelectorAll('.nav-item').forEach((btn) => {
  btn.addEventListener('click', () => {
    document.querySelectorAll('.nav-item').forEach((item) => item.classList.remove('active'));
    btn.classList.add('active');
    questionEl.value = btn.getAttribute('data-preset');
    questionEl.focus();
  });
});

studentIdEl.addEventListener('input', () => {
  currentSid.textContent = studentIdEl.value.trim() || 'S001';
});

sendBtn.addEventListener('click', ask);
resetBtn.addEventListener('click', resetConversation);
questionEl.addEventListener('keydown', (e) => {
  if ((e.ctrlKey || e.metaKey) && e.key === 'Enter') ask();
});
